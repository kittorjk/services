        if ((is_null($user))||(!$user->id)) {
            return View('app.index', ['service'=>'staff', 'user'=>null]);
        }
        if($user->acc_staff==0)
            return redirect()->action('LoginController@logout', ['service' => 'staff']);

        $records = User::orderBy('name')->paginate(20);

        $service = Session::get('service');

        return View::make('app.staff_brief', ['records' => $records, 'service' => $service, 'user' => $user]);
    }

